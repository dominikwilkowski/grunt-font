'use strict';

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
// Dependencies
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
const CFonts = require('cfonts');


//--------------------------------------------------------------------------------------------------------------------------------------------------------------
// Grunt plugin
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
module.exports = exports = function( grunt ) {
	grunt.registerMultiTask( 'font', 'Output in a nice font to the console', function() {
		CFonts.say( this.data.text, this.options() );
	});
};
